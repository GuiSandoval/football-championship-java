package Entidades;



public class Rodada {
    private int cd_rodada;
    private String descricao;
    
    public Rodada(){
        
    }

    public int getCd_rodada() {
        return cd_rodada;
    }

    public void setCd_rodada(int cd_rodada) {
        this.cd_rodada = cd_rodada;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }
    
    
}
