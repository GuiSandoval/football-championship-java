
package Entidades;

public class Time {
    private int cd_time;
    private String nome;
    private int pontos;
    private int golFeito;
    private int golSofre;

    public int getCd_time() {
        return cd_time;
    }

    public void setCd_time(int cd_time) {
        this.cd_time = cd_time;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getPontos() {
        return pontos;
    }

    public void setPontos(int pontos) {
        this.pontos = pontos;
    }

    public int getGolFeito() {
        return golFeito;
    }

    public void setGolFeito(int golFeito) {
        this.golFeito = golFeito;
    }

    public int getGolSofre() {
        return golSofre;
    }

    public void setGolSofre(int golSofre) {
        this.golSofre = golSofre;
    }
    
}
